package chap_01;

public class _02_DataTypes {
    public static void main(String[] args) {
        System.out.println("Hello World"); // 문자열
        System.out.println("안녕하세요?"); // 문자열
        System.out.println(12); // 숫자
        System.out.println(-34); // 숫자
        System.out.println(3.14); // 숫자
        System.out.println(true); // bolean
        System.out.println(false); // bolean

        System.out.println(123 + 345);
    }

    }
